          <p class="font-bold my-4">
            3. Phạm vi sử dụng và tiết lộ dữ liệu cá nhân của bạn
          </p>
          <p>
            Mục đích sử dụng
            <br /><br />
            3.1. Chúng tôi sử dụng và tiết lộ dữ liệu cá nhân theo các mục đích
            bên dưới phù hợp với pháp luật địa phương được áp dụng, và sau khi
            nhận được sự đồng ý của bạn nếu pháp luật có yêu cầu.
            <br /><br />
            3.2. Tùy thuộc vào mối quan hệ giữa bạn và chúng tôi (chẳng hạn, với
            tư cách là một Khách Hàng, Nhà Bán Hàng, một người truy cập vào các
            trang web của chúng tôi, hoặc một người sử dụng ứng dụng di động của
            chúng tôi), dữ liệu cá nhân mà chúng tôi thu thập từ bạn hoặc thông
            qua các bên thứ ba có thể được chúng tôi sử dụng cho một số mục đích
            nhất định như sau:<br /><br />
            (a) Để hỗ trợ việc bạn sử dụng các Dịch Vụ hoặc truy cập vào Nền
            Tảng. <br /><br />(b) Để quản trị và quản lý tài khoản trực tuyến mà
            bạn khởi tạo với chúng tôi. <br /><br />(c) Để thực hiện việc phân
            tích, nghiên cứu, hoạt động kinh doanh và phát triển, bao gồm:<br /><br />
            • Để hiểu hơn về trải nghiệm người dùng đối với các Dịch Vụ và Nền
            Tảng; <br /><br />• Để cải thiện bố cục hoặc nội dung của các trang
            trong Nền Tảng và tùy chỉnh chúng cho các người dùng;<br /><br />
            • Để xác định những người truy cập vào Nền Tảng;<br /><br />
            • Để thực hiện khảo sát, bao gồm thực hiện nghiên cứu về nhân khẩu
            học và hành vi của người dùng của chúng tôi để cải tiến công nghệ
            hiện tại của chúng tôi (ví dụ: công nghệ nhận dạng giọng nói, v.v.)
            thông qua học máy hoặc các phương thức khác;<br /><br />
            • Để trích lọc được thêm các đặc điểm liên quan đến bạn, nhằm cung
            cấp đến bạn các thông tin trọng tâm và/hoặc liên quan hơn từ Nền
            Tảng hoặc các kênh truyền thông khác của Lazada.<br /><br />
            • Để tiến hành phân tích dữ liệu, thử nghiệm và nghiên cứu, giám sát
            và phân tích xu hướng sử dụng và hoạt động để phát triển các sản
            phẩm và dịch vụ của chúng tôi;<br /><br />
            • Để phát triển thêm các sản phẩm và dịch vụ của chúng tôi.<br /><br />
            (d) Các mục đích pháp lý và vận hành<br /><br />
            • Để xác định danh tính của bạn liên quan đến mục đích phát hiện
            gian lận;<br /><br />
            • Để so sánh thông tin, và xác minh với bên thứ ba để đảm bảo rằng
            thông tin là chính xác;<br /><br />
            • Để xử lý khiếu nại, phản hồi, thực hiện chế tài và yêu cầu tháo gỡ
            liên quan đến bất kỳ nội dung nào mà bạn đã đăng tải trên Nền
            Tảng;<br /><br />
            • Để thống kê và nghiên cứu cho các yêu cầu báo cáo nội bộ và báo
            cáo theo quy định pháp luật và/hoặc yêu cầu lưu trữ hồ sơ;<br /><br />
            • Để thu thập, ghi, phân tích, xác nhận, lưu trữ, chỉnh sửa, công
            khai, kết hợp, truy cập, truy xuất, thu hồi, mã hóa, giải mã, sao
            chép, chia sẻ, truyền đưa, cung cấp, chuyển giao, xóa, hủy dữ liệu
            cá nhân của bạn cho các mục đích vận hành, pháp lý hoặc kinh
            doanh;<br /><br />
            • Để chia sẻ dữ liệu cá nhân liên quan đến bất kỳ việc mua bán, mua
            bán sáp nhập dự kiến nào đối với bất kỳ phần nào của hoạt động kinh
            doanh của chúng tôi, với điều kiện là chúng tôi đáp ứng các yêu cầu
            theo pháp luật về bảo vệ dữ liệu hiện hành khi tiết lộ dữ liệu cá
            nhân của bạn;<br /><br />
            • Để ngăn chặn hoặc điều tra bất kỳ hành vi vi phạm thực tế hoặc
            nghi ngờ vi phạm nào đối với Điều Khoản Sử Dụng, Chính Sách Bảo Mật
            hoặc bất kỳ chính sách nào của chúng tôi, gian lận, hoạt động bất
            hợp pháp, thiếu sót hoặc thực hiện không đúng, dù liên quan đến việc
            bạn sử dụng Dịch Vụ của chúng tôi hoặc bất kỳ vấn đề nào khác phát
            sinh từ mối quan hệ của bạn với chúng tôi;<br /><br />
            • Để thực hiện việc kiểm tra thẩm định; <br /><br />• Để tuân thủ
            các yêu cầu pháp lý và quy định (bao gồm, nếu áp dụng, việc hiển thị
            tên, thông tin liên hệ và thông tin công ty của bạn), bao gồm bất kỳ
            yêu cầu của cơ quan thực thi pháp luật nào, các yêu cầu của bất kỳ
            cơ quan chính phủ hoặc cơ quan quản lý nào có thẩm quyền đối với
            Lazada, liên quan đến bất kỳ thủ tục pháp lý nào hoặc trong trường
            hợp chúng tôi cho là cần thiết; và <br /><br />• Để bảo vệ tính
            mạng, sức khỏe hoặc sự an toàn cá nhân của nhân viên Lazada, những
            người dùng của Lazada và/hoặc của cộng đồng. <br /><br />(e) Bất kỳ
            mục đích nào khác mà đã có được chấp thuận của bạn, tuân thủ với các
            nghĩa vụ của chúng tôi theo pháp luật hiện hành, bao gồm việc thông
            báo cho bạn tại thời điểm xin chấp thuận. <br /><br />3.3. Trường
            hợp bạn là Khách Hàng và/hoặc bạn đã đăng nhập hoặc đăng ký với
            chúng tôi, dữ liệu cá nhân mà chúng tôi thu thập từ bạn hoặc thông
            qua bên thứ ba có thể được sử dụng cho các mục đích khác như sau:<br /><br />
            (a) Xử lý đơn đặt hàng sản phẩm của bạn (dù được bán bởi chúng tôi
            hoặc nhà bán hàng thứ ba)<br /><br />
            • Để xử lý các đơn đặt hàng mà bạn đặt thông qua Nền Tảng;
            <br /><br />• Để giao các sản phẩm bạn đã mua thông qua Nền Tảng.
            Chúng tôi có thể chuyển thông tin của bạn cho một bên thứ ba (ví dụ
            đối tác vận chuyển của chúng tôi) hoặc cơ quan có thẩm quyền liên
            quan (ví dụ cơ quan hải quan) để thực hiện giao sản phẩm cho bạn;<br /><br />
            • Để cập nhật cho bạn về việc giao các sản phẩm;<br /><br />
            • Để cung cấp hỗ trợ khách hàng cho các đơn đặt hàng của bạn, chẳng
            hạn, để hoàn trả sản phẩm mà bạn đã mua thông qua Nền Tảng; và<br /><br />
            • Để xác minh và thực hiện các giao dịch thanh toán (bao gồm các
            khoản thanh toán bằng thẻ tín dụng, chuyển khoản ngân hàng, thanh
            toán tiền mặt, chuyển tiền hoặc giao dịch ví điện tử) liên quan đến
            các khoản thanh toán liên quan đến bạn, các chương trình xúc tiến mà
            bạn tham gia và/hoặc các Dịch Vụ do bạn sử dụng. <br /><br />• Để
            xác minh và thực hiện các giao dịch thanh toán đó, thông tin thanh
            toán mà có thể bao gồm dữ liệu cá nhân của bạn, sẽ được chuyển cho
            các bên thứ ba như là các bên cung cấp dịch vụ thanh toán của chúng
            tôi.<br /><br />
            (b) Cung cấp các Dịch Vụ<br /><br />
            • Để hiển thị tên, tên người dùng hoặc hồ sơ của bạn trên Nền Tảng
            (bao gồm trên bất kỳ đánh giá nào được đăng bởi bạn);<br /><br />
            • Để hỗ trợ việc sử dụng các ứng dụng nhỏ có sẵn trên Nền Tảng và
            việc mua hàng thực hiện thông qua các ứng dụng nhỏ này;<br /><br />
            • Để phản hồi các thắc mắc, phản hồi, khiếu nại hoặc tranh chấp của
            bạn, dù trực tiếp hay thông qua các bên cung cấp dịch vụ thứ ba của
            chúng tôi;<br /><br />
            • Để hiển thị trên bảng điểm trên Nền Tảng liên quan đến các chiến
