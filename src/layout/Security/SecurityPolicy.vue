            Vụ Cấp Tín Dụng cho bạn. Thông qua Nền Tảng Lazada, chúng tôi có thể
            hỗ trợ bằng cách thu thập thông tin đó thay mặt Đối Tác của chúng
            tôi hoặc tiết lộ thông tin đó cho Đối Tác của chúng tôi, trong từng
            trường hợp theo các thỏa thuận được ký kết với Đối Tác, luật và quy
            định áp dụng và khi được bạn đồng ý. Các Đối Tác này là các bên thứ
            ba độc lập, và do đó việc thu thập, sử dụng, lưu giữ, xử lý và tiết
            lộ Dữ Liệu Cá Nhân của bạn sẽ tuân theo các chính sách bảo mật áp
            dụng và các điều khoản và điều kiện của Đối Tác chúng tôi.
            <br /><br />
            Bạn có trách nhiệm xem xét cẩn thận mọi chính sách bảo mật và các
            điều khoản và điều kiện áp dụng của Đối Tác của chúng tôi trước khi
            đồng ý với các chính sách và điều khoản và điều kiện đó và đăng ký
            bất kỳ Dịch Vụ Cấp Tín Dụng nào do (các) Đối Tác có liên quan cung
            cấp.
          </p>

          <p class="font-bold my-4">2. Thu thập Dữ Liệu Cá Nhân</p>
          <br /><br />
          2.1. Khi bạn đăng ký truy cập hoặc sử dụng Dịch Vụ Cấp Tín Dụng
          và/hoặc trong quá trình bạn truy cập hoặc sử dụng Dịch Vụ Cấp Tín
          Dụng, tùy thuộc vào các dịch vụ mà bạn đăng ký sử dụng và/hoặc sử
          dụng, chúng tôi có thể thu thập Dữ Liệu Cá Nhân, một vài trong số đó
          (được in đậm và gạch chân dưới đây) là dữ liệu cá nhân nhạy cảm, trực
          tiếp hoặc gián tiếp từ bạn hoặc các bên thứ ba khác (ngoài thông tin
          được liệt kê trong Chính Sách Bảo Mật Nền Tảng Lazada), như sau:
          <br /><br />
          (a) Thông Tin Cá Nhân và Thông Tin Liên Hệ, có thể bao gồm thông tin
          về địa chỉ cư trú hoặc giao hàng, tình trạng hôn nhân, giới tính, ngày
          và nơi sinh, quốc tịch, địa chỉ email, trình độ học vấn của bạn, tên
          của người đại diện hoặc người được uỷ quyền/giám đốc hành động nhân
          danh bạn, số thành viên gia đình và con cái, vợ/chồng, thông tin liên
          hệ khẩn cấp, tài khoản mạng xã hội.
          <br /><br />
          (b) Thông Tin Nhận Dạng, có thể bao gồm ảnh của bạn, các tài liệu
          và/hoặc số thẻ định danh cá nhân do chính phủ cấp (ví dụ: số thẻ định
          danh, hộ chiếu hoặc các giấy tờ thông hành khác); và nếu có thể, thông
          tin bổ sung cho mục đích xác minh (ví dụ: bằng chứng về địa chỉ của
          bạn).
          <br /><br />
          (c) Thông Tin Việc Làm Trong Quá Khứ và Hiện Tại, có thể bao gồm các
          thông tin như nghề nghiệp, tên người sử dụng lao động, loại hình tổ
          chức của người sử dụng lao động, lĩnh vực nghề nghiệp, chức năng và
          trách nhiệm công việc, chức danh, mức lương.
          <br /><br />
          (d) Thông Tin Tài Chính, có thể bao gồm chi tiết tài khoản ngân hàng,
          bằng chứng thu nhập, thông tin về các khoản vay của bạn bao gồm bất kỳ
          hạn mức tín dụng, lịch sử hoàn trả khoản vay, thông tin về hồ sơ rủi
          ro, xếp hạng tín dụng và khả năng thanh toán của bạn, thông tin theo
          bất kỳ tuyên bố nào về tính phù hợp, tính phù hợp của giao dịch và bất
          kỳ chi tiết tài chính nào khác.
          <br /><br />
          (e) Dữ Liệu Kỹ Thuật, có thể bao gồm thông tin thu được từ máy tính,
          thiết bị di động hoặc phần cứng khác mà qua đó bạn truy cập và sử dụng
          Dịch Vụ Cấp Tín Dụng (chẳng hạn như địa chỉ IP, vị trí địa lý, mã nhận
          dạng thiết bị của bạn và các tính năng/dữ liệu khác có thể được sử
          dụng để xác định thiết bị, loại trình duyệt/nền tảng và phiên bản, nhà
          cung cấp dịch vụ internet, hệ điều hành, nguồn giới thiệu/trang thoát,
          thời lượng truy cập/sử dụng, lượt xem trang và thông tin thiết bị khác
          có được từ thiết bị của bạn).
          <br /><br />
          (f) Dữ Liệu Sinh Trắc Học, chẳng hạn như dữ liệu hình ảnh khuôn mặt,
          để xác minh danh tính của bạn.
          <br /><br />
          (g) Thông tin mà Lazada cho là cần thiết để tạo điều kiện thuận lợi
          cho việc cung cấp Dịch Vụ Cấp Tín Dụng cho bạn, bao gồm thông tin cần
          thiết để vận hành tài khoản của bạn và/hoặc để thực hiện các nghĩa vụ
          của Lazada theo luật áp dụng.
          <br /><br />
          (h) Thông tin khác cần thiết để Đối Tác của chúng tôi cung cấp và vận
          hành Dịch Vụ Cấp Tín Dụng và đưa ra quyết định liên quan đến Dịch Vụ
          Cấp Tín Dụng (chẳng hạn như để đánh giá xếp hạng tín dụng, khả năng
          thanh toán hoặc đáp ứng điều kiện nhận Dịch Vụ Cấp Tín Dụng của bạn
          hoặc liên quan đến việc gia hạn hoặc điều chỉnh khoản vay, tín dụng
          hoặc trả góp) hoặc để thực hiện các nghĩa vụ của Đối Tác chúng tôi
          theo luật áp dụng.
          <br /><br />
          (i) Thông tin về bạn từ việc bạn sử dụng Nền Tảng Lazada và/hoặc Dịch
          Vụ Cấp Tín Dụng, có thể bao gồm thông tin về hành vi mua hàng của bạn
          và thông tin mà chúng tôi và/hoặc Đối Tác của chúng tôi nhận được khi
          đưa ra quyết định về dịch vụ cho bạn.
          <br /><br />
          2.2. Chúng tôi đã thực hiện các bước để đảm bảo rằng chúng tôi không
          thu thập nhiều Dữ Liệu Cá Nhân từ bạn hơn mức cần thiết để chúng tôi
          thực hiện các bước tiền hợp đồng theo yêu cầu của bạn hoặc do bạn khởi
          tạo hoặc để tuân thủ các nghĩa vụ hợp đồng của chúng tôi đối với bạn
          và/hoặc Đối Tác của chúng tôi, để phục vụ các mục đích được nêu trong
          Mục 3 của Chính Sách Bảo Mật Thanh Toán Lazada này, để bảo vệ tài
          khoản của bạn, để giảm thiểu và ngăn chặn gian lận, để chặn hoặc ngăn
          chặn mối nguy hiểm đối với tính mạng, cơ thể hoặc sức khỏe của bất kỳ
          người nào, để tuân thủ các nghĩa vụ pháp lý của chúng tôi, để thực
          hiện một nhiệm vụ được tiến hành vì lợi ích công cộng hoặc cần thiết
          để thực thi thẩm quyền chính thức, để bảo vệ các quyền hợp pháp của
          bạn và của chúng tôi, để tiến hành thiết lập, tuân thủ, thực thi hoặc
          bảo vệ các yêu cầu pháp lý, để đạt được lợi ích hợp pháp của chúng tôi
          hoặc của những người khác, và để điều hành hoạt động kinh doanh của
          chúng tôi.
          <br /><br />
          2.3. Việc cung cấp Dữ Liệu Cá Nhân của bạn là tự nguyện. Tuy nhiên,
          nếu bạn chọn không cung cấp thông tin theo yêu cầu, chúng tôi không
          thể cung cấp cho bạn các sản phẩm hoặc dịch vụ nếu có.
          <br /><br />
          2.4. Trong một số trường hợp, bạn có thể cung cấp Dữ Liệu Cá Nhân của
          các cá nhân khác (chẳng hạn như vợ/chồng, thành viên gia đình hoặc bạn
          bè của bạn) cho chúng tôi. Ví dụ: bạn có thể thêm những người này làm
          “liên hệ khẩn cấp” hoặc “liên hệ nguy cấp”. Nếu bạn cung cấp cho chúng
          tôi Dữ Liệu Cá Nhân của những người này, bạn cam đoan và đảm bảo rằng
          bạn đã nhận được sự đồng ý của những người này để Dữ Liệu Cá Nhân của
          họ được thu thập, sử dụng và tiết lộ như được quy định trong Chính
          Sách Bảo Mật Nền Tảng Lazada và Chính Sách Bảo Mật Thanh Toán Lazada
          này, bao gồm cả việc chuyển giao Dữ Liệu Cá Nhân của những người này
          ra bên ngoài lãnh thổ Việt Nam.
          <br /><br />
          3. Sử dụng Dữ Liệu Cá Nhân
          <br /><br />
          3.1. Ngoài các mục đích sử dụng được liệt kê trong Chính Sách Bảo Mật
          Nền Tảng Lazada, chúng tôi có thể sử dụng Dữ Liệu Cá Nhân của bạn để
          hỗ trợ việc cung cấp Dịch Vụ Cấp Tín Dụng cho bạn và cũng như cho các
          hoạt động kinh doanh khác của Lazada, bao gồm các mục đích sau:
          <br /><br />
          (a) Xử lý, quản lý hoặc xác minh đăng ký của bạn để trở thành người
          dùng Dịch Vụ Cấp Tín Dụng, có thể bao gồm:
          <br /><br />
          (i) Xác minh danh tính của bạn.
          <br /><br />
          (ii) Xác minh thông tin do bạn cung cấp.
          <br /><br />
          (iii) Thực hiện tìm kiếm tham chiếu trong thông tin do bạn cung cấp
          cho chúng tôi hoặc thông tin về bạn từ bên thứ ba.
          <br /><br />
          (iv) Đưa ra hoặc hỗ trợ đưa ra các quyết định liên quan đến việc bạn
          đăng ký trở thành người sử dụng Dịch Vụ Cấp Tín Dụng (bao gồm nhưng
          không giới hạn các quyết định liên quan đến thẩm định khách hàng) hoặc
          sử dụng hợp pháp bất kỳ tính năng và chức năng nào của Dịch Vụ Cấp Tín
          Dụng.
          <br /><br />
          (v) Duy trì và quản lý đăng ký của bạn với tư cách là người dùng Dịch
          Vụ Cấp Tín Dụng.
          <br /><br />
          (b) Cung cấp cho bạn hỗ trợ khách hàng liên quan đến việc bạn sử dụng
          Dịch Vụ Cấp Tín Dụng, có thể bao gồm:
          <br /><br />
          (i) Quản lý và tạo điều kiện thuận lợi cho việc bạn sử dụng Dịch Vụ
          Cấp Tín Dụng.
          <br /><br />
          (ii) Xác thực, xử lý, quản lý và/hoặc hoàn thành các giao dịch và/hoặc
          đơn vay, rút tiền, giải ngân và/hoặc trả nợ của bạn (theo sự chấp
          thuận của Đối Tác có liên quan).
          <br /><br />
          (iii) Gửi cho bạn thông báo về các giao dịch và/hoặc đơn vay, rút
          tiền, giải ngân và/hoặc trả nợ của bạn (theo sự chấp thuận của Đối Tác
          có liên quan).
          <br /><br />
          (iv) Trả lời các yêu cầu, thắc mắc, phản hồi, khiếu nại hoặc tranh
          chấp của bạn.
          <br /><br />
          (c) Đối với mục đích đánh giá rủi ro và quản lý vận hành, có thể bao
          gồm:
          <br /><br />
          (i) Quản lý rủi ro, thực hiện kiểm tra mức độ tín nhiệm và khả năng
          thanh toán, hoặc đánh giá, phát hiện, điều tra, ngăn chặn và/hoặc khắc
          phục gian lận hoặc các hoạt động có khả năng bị cấm hoặc bất hợp pháp
          khác và bảo vệ tính toàn vẹn của Dịch Vụ Cấp Tín Dụng.
          <br /><br />
          (ii) Lấy vị trí địa lý của bạn để xác minh tính khả dụng của dịch vụ.
          <br /><br />
          (iii) Thông báo cho bạn về bất kỳ sự cố dịch vụ nào và/hoặc các hành
          động hoặc giao dịch tài khoản bất thường.
          <br /><br />
          (iv) Duy trì và cung cấp bất kỳ bản cập nhật phần mềm và/hoặc các bản
          cập nhật và hỗ trợ khác có thể được yêu cầu tùy từng thời điểm để đảm
          bảo Dịch Vụ Cấp Tín Dụng vận hành trơn tru.
          <br /><br />
          (v) Xử lý hoặc hỗ trợ dịch vụ khách hàng, thực hiện các hướng dẫn của
          bạn, xử lý hoặc trả lời bất kỳ câu hỏi nào do (hoặc được cho là do)
          bạn hoặc người thay mặt bạn đưa ra.
          <br /><br />
          (vi) Liên hệ với bạn hoặc liên lạc với bạn và bất kỳ liên hệ nào bạn
          chia sẻ với chúng tôi bằng các cuộc gọi điện thoại di động và
          internet, tin nhắn văn bản, email và/hoặc thư bưu điện hoặc các phương
          tiện khác nhằm mục đích duy trì và/hoặc quản lý mối quan hệ của bạn
          với chúng tôi hoặc việc bạn sử dụng các Dịch Vụ Cấp Tín Dụng.
          <br /><br />
          (vii) Phát hiện, điều tra, ngăn chặn, khắc phục và/hoặc thực thi các
          quyền của chúng tôi liên quan đến bất kỳ hành vi vi phạm thực tế hoặc
          nghi ngờ nào đối với các điều khoản và điều kiện dịch vụ, bất kỳ chính
          sách nội bộ áp dụng, các tiêu chuẩn, hướng dẫn, luật hoặc quy định của
          ngành có liên quan.
          <br /><br />
          (viii) Đánh giá và xác minh sự tuân thủ của bạn với các điều khoản và
          điều kiện của Dịch Vụ Cấp Tín Dụng.
          <br /><br />
          (ix) Thực hiện kiểm toán, nghiên cứu, phân tích thống kê hoặc khảo
          sát, dù bằng lời nói hay bằng văn bản, để quản lý và bảo vệ hoạt động
          kinh doanh của chúng tôi, bao gồm cả cơ sở hạ tầng công nghệ thông tin
          của chúng tôi, để đo lường hiệu suất của Dịch Vụ Cấp Tín Dụng và để
          nâng cao sự hài lòng của bạn đối với Dịch Vụ Cấp Tín Dụng.
          <br /><br />
          (x) Triển khai, giám sát, quản lý và phân tích Dịch Vụ Cấp Tín Dụng,
          bao gồm cả việc cải thiện các dịch vụ đó hoặc phát triển các sản phẩm
          và dịch vụ mới.
          <br /><br />
          (xi) Phân tích các xu hướng, cách sử dụng và hành vi khác (dù là trên
          cơ sở riêng lẻ hay tập thể), giúp chúng tôi hiểu rõ hơn về cách bạn và
          dữ liệu người dùng tập thể của chúng tôi truy cập và sử dụng Dịch Vụ
          Cấp Tín Dụng và các hoạt động thương mại cơ bản, bao gồm cả mục đích
          cải thiện Dịch Vụ Cấp Tín Dụng và trả lời các câu hỏi và sở thích của
          bạn.
          <br /><br />
          (xii) Cho phép thẩm định và đánh giá hoặc thẩm định khác liên quan đến
          bất kỳ đề xuất mua hàng, giao dịch tài trợ tài chính hoặc liên doanh,
          sáp nhập hoặc mua lại bất kỳ bộ phận hoặc toàn bộ hoạt động kinh doanh
          của chúng tôi, hoặc trong trường hợp tái cơ cấu, tổ chức lại, giải thể
          hoặc bán hoặc chuyển nhượng khác một số hoặc toàn bộ tài sản của
          Lazada (dù là trên cơ sở doanh nghiệp tiếp tục hoạt động hay là một
          phần của phá sản, kiện tụng hoặc thủ tục tố tụng tương tự), kể cả
          trong các trường hợp Dữ Liệu Cá Nhân của bạn nằm trong số các tài sản
          được chuyển giao với điều kiện là chúng tôi đáp ứng các yêu cầu của
          pháp luật áp dụng về bảo vệ dữ liệu.
          <br /><br />
