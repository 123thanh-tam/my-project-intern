            (n) Khi bất kỳ người dùng nào khác trên Nền Tảng đăng bất kỳ nhận
            xét nào về nội dung mà bạn đã đăng tải lên Nền Tảng hoặc khi bạn
            đăng bất kỳ nhận xét nào về nội dung của người dùng khác đã đăng tải
            lên Nền Tảng;
            <br /><br />
            (o) Khi một bên thứ ba gửi khiếu nại về bạn hoặc nội dung mà bạn đã
            đăng tải lên Nền Tảng; và
            <br /><br />
            (p) Khi bạn trực tiếp tương tác với chúng tôi, bao gồm khi bạn tương
            tác với các nhân viên dịch vụ khách hàng được thuê ngoài của chúng
            tôi.
            <br /><br />
            2.6 Chúng tôi có thể thu thập dữ liệu cá nhân từ bạn, các bên thứ ba
            (bao gồm nhưng không giới hạn các đại lý, bên cung cấp, nhà thầu,
            đối tác và bất kỳ người nào khác cung cấp dịch vụ cho chúng tôi mà
            những đơn vị này thu thập thông tin cá nhân của bạn và/hoặc thay mặt
            chúng tôi thực hiện các chức năng hoặc với những người mà chúng tôi
            cộng tác, bao gồm nhưng không giới hạn các bên cung cấp dịch vụ
            thanh toán, các nguồn dữ liệu của chính phủ, các bên cung cấp dịch
            vụ tài chính, các tổ chức tín dụng, các đối tác cung cấp dịch vụ
            giao hàng, tiếp thị và các dịch vụ khác), các bên liên kết của chúng
            tôi hoặc dữ liệu đó có thể được thu thập tự động khi bạn sử dụng Nền
            Tảng hoặc các Dịch Vụ, như được trình bày trong phần này. Vui lòng
            xem thêm các Mục 2.11 đến 2.15 về thu thập dữ liệu máy tính.
            <br /><br />
            2.7 Bạn đồng ý chỉ gửi dữ liệu cá nhân chính xác và không gây nhầm
            lẫn và đồng ý cập nhật các thông tin này và thông báo cho chúng tôi
            về bất kỳ thay đổi nào đối với dữ liệu cá nhân mà bạn đã cung cấp
            cho chúng tôi. Chúng tôi sẽ có quyền yêu cầu cung cấp tài liệu và
            thực hiện việc kiểm tra cần thiết để xác minh dữ liệu cá nhân do bạn
            cung cấp như là một phần của quy trình xác minh người dùng của chúng
            tôi hoặc theo quy định của pháp luật.
            <br /><br />
            2.8 Chúng tôi sẽ chỉ có thể thu thập một số danh mục dữ liệu cá nhân
            nhất định nếu bạn tự nguyện cung cấp dữ liệu cá nhân đó cho chúng
            tôi hoặc theo cách khác được quy định tại Chính Sách Bảo Mật này.
            Nếu bạn lựa chọn không cung cấp dữ liệu cá nhân của bạn cho chúng
            tôi hoặc sau đó rút lại sự đồng ý của bạn đối với việc chúng tôi sử
            dụng dữ liệu cá nhân của bạn, chúng tôi có thể sẽ không cung cấp
            được cho bạn một số tính năng hoặc chức năng nhất định trên các Dịch
            Vụ hoặc việc truy cập Nền Tảng.
            <br /><br />
            2.9 Trong một số trường hợp, bạn có thể cung cấp dữ liệu cá nhân của
            các cá nhân khác cho chúng tôi (chẳng hạn thành viên gia đình hoặc
            bạn bè hoặc người trong danh sách liên lạc của bạn), khi bạn sử dụng
            tính năng “Tìm Kiếm Bạn Bè” hoặc tính năng tương tự, hoặc khi bạn
            thêm các cá nhân này vào danh sách người nhận hoặc người thụ hưởng
            Dịch Vụ của chúng tôi. Bạn cam đoan và đảm bảo rằng bạn đã có được
            sự đồng ý, được cấp phép và được cho phép cần thiết từ các cá nhân
            đó để chia sẻ và chuyển dữ liệu cá nhân của họ cho chúng tôi, và cho
            việc chúng tôi thu thập, lưu trữ, sử dụng, tiết lộ hoặc xử lý bằng
            hình thức khác dữ liệu cá nhân đó theo Chính Sách Bảo Mật này.
            <br /><br />
            2.10 Nếu bạn đăng ký trở thành một người dùng trên Nền Tảng của
            chúng tôi bằng tài khoản mạng xã hội của bạn hoặc liên kết tài khoản
            Lazada với tài khoản mạng xã hội của bạn hoặc sử dụng một số tính
            năng mạng xã hội khác của Lazada, bạn đồng ý và chấp thuận rằng
            chúng tôi có thể truy cập dữ liệu cá nhân về bạn mà bạn đã tự nguyện
            cung cấp cho bên cung cấp dịch vụ mạng xã hội của bạn phù hợp với
            các chính sách của bên cung cấp dịch vụ đó và chúng tôi sẽ quản lý
            dữ liệu cá nhân của bạn phù hợp với Chính Sách Bảo Mật này.
            <br />
            Thu thập dữ liệu máy tính
            <br /><br />
            2.11 Chúng tôi hoặc các bên cung ứng dịch vụ được ủy quyền của chúng
            tôi có thể sử dụng cookie, web beacons và các công nghệ tương tự
            liên quan đến việc sử dụng các Dịch Vụ hoặc truy cập Nền Tảng của
            bạn.
            <br /><br />
            2.12 Khi bạn truy cập Nền Tảng thông qua máy tính, thiết bị di động,
            hoặc bất kỳ thiết bị nào khác có kết nối Internet, bạn xác nhận và
            đồng ý rằng các máy chủ của chúng tôi sẽ tự động ghi lại dữ liệu mà
            trình duyệt của bạn gửi đi bất cứ khi nào bạn truy cập một trang
            web, chẳng hạn như dữ liệu kỹ thuật và dữ liệu sử dụng được nêu
            trong Mục 2.3 và 2.4 ở trên.
            <br /><br />
            2.13 Dữ liệu này được thu thập cho việc phân tích và đánh giá nhằm
            giúp chúng tôi cải thiện trang web của chúng tôi và các dịch vụ và
            sản phẩm mà chúng tôi cung cấp, cũng như giúp chúng tôi cá nhân hóa
            nội dung để phù hợp với sở thích ưu tiên của bạn một cách nhanh
            chóng hơn. Dữ liệu cũng được thu thập để làm cho các Dịch Vụ và Nền
            Tảng trở nên thuận tiện và hữu ích hơn cho bạn, và để cung cấp quảng
            cáo phù hợp hơn liên quan đến các sản phẩm, dịch vụ và tính năng của
            thị trường cho bạn.
            <br /><br />
            2.14 Cookie là các tệp văn bản nhỏ (thường bao gồm các chữ cái và
            số) được đặt trong bộ nhớ của trình duyệt hoặc thiết bị của bạn khi
            bạn truy cập một trang web hoặc xem một tin nhắn. Cookie cho phép
            chúng tôi nhận biết một thiết bị hoặc trình duyệt cụ thể. Web
            beacons là những hình ảnh đồ họa nhỏ có thể được đưa vào các Dịch Vụ
            và Nền Tảng của chúng tôi. Web beacons cho phép chúng tôi đếm những
            người dùng đã xem các trang này để chúng tôi có thể hiểu rõ hơn về
            mối quan tâm và ưu tiên của bạn.
            <br /><br />
            2.15 Bạn có thể quản lý và xóa các cookie thông qua cài đặt trình
            duyệt hoặc thiết bị của bạn. Tuy nhiên, một số cookie nhất định được
            yêu cầu phải có để kích hoạt tính năng căn bản (chẳng hạn như thêm
            sản phẩm vào giỏ hàng của bạn), vì vậy xin lưu ý rằng việc thay đổi
            và xóa cookie có thể ảnh hưởng đến tính năng có sẵn trên Nền Tảng
            hoặc thông qua các Dịch Vụ của chúng tôi.
